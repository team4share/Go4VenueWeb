package com.go4venue.web.persistence.beans;

public class VenueURL {

    private Long key;
    private String url;
    /**
     * @return the key
     */
    public Long getKey() {
	return key;
    }
    /**
     * @param key the key to set
     */
    public void setKey(Long key) {
	this.key = key;
    }
    /**
     * @return the url
     */
    public String getUrl() {
	return url;
    }
    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
	this.url = url;
    }
}
